#include<stdio.h>
int main()
{
    int x;
    char f;
    scanf("%d",&x);
    if (x>=90)
        f='A';
    if ((x>=80)&&(x<90))
        f='B';
    if (x<80)
        f='C';
    printf("f=%c\n",f);
    return 0;
}
